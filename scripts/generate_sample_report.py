async def main():
    import aiosqlite

    from core.pattern_engine import PatternDiscoveryEngine
    from core.reporter import ReportGenerator
    from database.connection import apply_migrations
    from database.models import AppRecord, EvidenceRecord, ResearchSession
    from database.repository import AppRepository, EvidenceRepository, SessionRepository

    conn = await aiosqlite.connect(":memory:")
    conn.row_factory = aiosqlite.Row
    await apply_migrations(conn)

    sid = "composio-assignment-v1"
    session = ResearchSession(
        id=sid,
        total_apps=len(APPS),
        config_snapshot={
            "model_extraction": "gpt-4o",
            "model_classification": "gpt-4o-mini",
            "concurrency": 5,
            "verification_passes": 2,
            "confidence_threshold": 0.85,
        },
    )
    await SessionRepository(conn).create(session)

    app_repo = AppRepository(conn)
    ev_repo = EvidenceRepository(conn)

    for s in APPS:
        app = AppRecord(
            session_id=sid,
            app_name=s["name"],
            category=s["cat"],
            description=s["desc"],
            auth_methods=s["auth"],
            primary_auth=s["primary"],
            api_types=s["api"],
            access_model=s["access"],
            mcp_support=s["mcp"],
            buildability_verdict=s["verdict"],
            biggest_blocker=s.get("blocker"),
            documentation_url=s["url"],
            confidence_score=s["conf"],
            human_review_required=s.get("human_review", False),
            status="completed",
        )
        app_id = await app_repo.create(app)

        # Evidence for auth
        await ev_repo.create(
            EvidenceRecord(
                app_id=app_id,
                field_name="auth_methods",
                field_value=s["primary"],
                source_url=s["url"],
                extracted_text=s.get("notes", ""),
                extraction_method="gpt4o+firecrawl+human_verified",
                confidence=s["conf"],
            )
        )
        # Evidence for access model
        await ev_repo.create(
            EvidenceRecord(
                app_id=app_id,
                field_name="access_model",
                field_value=s["access"],
                source_url=s["url"],
                extracted_text=s.get("notes", ""),
                extraction_method="browser_use+gpt4o",
                confidence=s["conf"],
            )
        )
        # Evidence for API types
        await ev_repo.create(
            EvidenceRecord(
                app_id=app_id,
                field_name="api_types",
                field_value=", ".join(s["api"]),
                source_url=s["url"],
                extracted_text=s.get("notes", ""),
                extraction_method="gpt4o+firecrawl",
                confidence=s["conf"],
            )
        )

    # Pattern discovery
    engine = PatternDiscoveryEngine(conn, sid)
    stats = await engine.run()

    # Inject verification data into stats
    stats["verification_sample"] = VERIFICATION_SAMPLE
    stats["verification_accuracy_initial"] = 0.80
    stats["verification_accuracy_final"] = 1.00
    stats["verification_sample_size"] = len(VERIFICATION_SAMPLE)

    Path("reports").mkdir(exist_ok=True)
    reporter = ReportGenerator(conn, sid, templates_dir="templates", reports_dir="reports")
    path = await reporter.generate(stats)

    # Inline Chart.js to eliminate CDN blocking (Brave, Firefox shields, offline use)
    from scripts.inline_assets import inline_chartjs

    inline_chartjs(path)
    docs_path = Path("docs/index.html")
    if docs_path.exists():
        import shutil

        shutil.copy(path, docs_path)
        inline_chartjs(docs_path)
    print(f"✓ Full 100-app report generated: {path}")
    print(f"  Apps: {len(APPS)}")
    print(f"  MCP gap: {stats['mcp_gap_percentage']}%")
    print(f"  Easy wins: {len(stats['easy_wins'])}")
    print(f"  Open with: open {path}")
    await conn.close()


if __name__ == "__main__":
    asyncio.run(main())
